"""
Циклы - повторяющиеся действия в программе.

Кумире:
нц 4 раза
    ...
кц
"""

for i in range(4):
    print(i)

print("Конец цикла.")

for i in range(10, 15):
    print(i)

print("Конец цикла.")

for i in range(10, 20, 2):
    print(i)

print("Конец цикла.")

for num in range(20, 15, -1):
    print(num)

print("Конец цикла.")
